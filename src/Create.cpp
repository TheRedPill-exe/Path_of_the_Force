void saveCharacterToFile(const Character& character, const char* fileName) {
    FILE* user = fopen(fileName, "wb");
    if (user) {
        fwrite(&character, sizeof(Character), 1, user);
        fclose(user);
        cout << "Character saved to " << fileName << endl;
    } else {
        cout << "Error opening file: " << fileName << endl;
    }
}

